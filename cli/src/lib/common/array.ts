/**
 * Return last element
 * @private
 * @param {(Array<any>)} array
 * @returns object | undefined
 */
export const lastOf = (array: Array<any>) => array[array.length - 1];

/**
 * Return first element
 * @private
 * @param {(Array<any>)} array
 * @returns object | undefined
 */
export const firstOf = (array: Array<any>) => array[0];
